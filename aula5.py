#Operadores aritméticos em python (bem básico)
adicao = 10 + 10
print('Adicão ', adicao)

subtracao = 10 - 5
print('Subtração ', subtracao)

divisao = 10 / 2.2 #Retorno da divisão sempre vai ser Float
print('Divisão ', divisao)

divisao_inteira = 10 // 2 #Ignora os decimais, pega somente o número inteiro do resultado sem resto.
print("Divisão inteira", divisao_inteira)

exponenciacao = 2 ** 10
print('Exponenciação ', exponenciacao)

modulo = 55 % 2 # faz a divisão e retorna a sobra, se não houver retorna 0
print("Módulo ", modulo) 


